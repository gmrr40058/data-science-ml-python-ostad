# 🎬 Scene 4: Set Battle – Union, Intersection, Difference
# 🐒 Liston and 🐘 Tupletta challenge 🦁 Setty to a fruit battle.


jungle_set1 = {'banana', 'apple', 'mango'}
jungle_set2 = {'apple', 'grape', 'kiwi'}


# 🦁 Setty:
# “Watch my jungle tricks!”

# Union (|) – “All fruits, all friends!”

print(jungle_set1 | jungle_set2)  # {'banana', 'apple', 'mango', 'grape', 'kiwi'}



# Intersection (&) – “Only common fruits enter my club!”

print(jungle_set1 & jungle_set2)  # {'apple'}


# Difference (-) – “Fruits only I have, others don’t!”

print(jungle_set1 - jungle_set2)  # {'banana', 'mango'}


